[CmdletBinding()]
param(
  [string]$LogDir = 'C:\ProgramData\FirewallCore\Logs',
  [int]$Top = 8,
  [int]$Tail = 120
)

$ErrorActionPreference = 'Stop'

if (-not (Test-Path -LiteralPath $LogDir)) {
  Write-Warning ('Log directory not found: {0}' -f $LogDir)
  return
}

$files = Get-ChildItem -LiteralPath $LogDir -File -ErrorAction SilentlyContinue
if (-not $files) {
  Write-Warning ('No log files found in {0}' -f $LogDir)
  return
}

$sorted = $files | Sort-Object LastWriteTime -Descending

$sorted |
  Select-Object -First $Top |
  Format-Table Name, Length, LastWriteTime -AutoSize

$latest = $sorted | Select-Object -First 1
Write-Host ''
Write-Host ('=== TAIL: {0} ===' -f $latest.FullName)

# Encoding left default to avoid surprises across mixed log emitters.
Get-Content -LiteralPath $latest.FullName -Tail $Tail
